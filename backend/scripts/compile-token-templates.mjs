/**
 * Compile the ERC-20 launcher templates to a committed artifact (bytecode + ABI) so the
 * runtime never needs a Solidity compiler. Re-run after editing contracts/CeloMindTokens.sol:
 *   npm i -D solc && node scripts/compile-token-templates.mjs
 */
import solc from "solc";
import { readFileSync, writeFileSync } from "node:fs";

const SOURCE = "contracts/CeloMindTokens.sol";
const OUT = "packages/mcp-server/src/token-artifacts.ts";

const input = {
  language: "Solidity",
  sources: { "CeloMindTokens.sol": { content: readFileSync(SOURCE, "utf8") } },
  settings: {
    optimizer: { enabled: true, runs: 200 },
    outputSelection: { "*": { "*": ["abi", "evm.bytecode.object"] } },
  },
};

const out = JSON.parse(solc.compile(JSON.stringify(input)));
const fatal = (out.errors ?? []).filter((e) => e.severity === "error");
for (const e of out.errors ?? []) console.error(e.formattedMessage);
if (fatal.length) process.exit(1);

const compiled = out.contracts["CeloMindTokens.sol"];
const artifact = {};
for (const name of ["CeloMindFixedToken", "CeloMindMintableToken"]) {
  const c = compiled[name];
  artifact[name] = { abi: c.abi, bytecode: "0x" + c.evm.bytecode.object };
}
const ts =
  `// AUTO-GENERATED by scripts/compile-token-templates.mjs — do not edit by hand.\n` +
  `// Source: contracts/CeloMindTokens.sol (solc ${solc.version()})\n` +
  `export type TokenArtifact = { abi: readonly unknown[]; bytecode: \`0x\${string}\` };\n` +
  `export const TOKEN_ARTIFACTS: Record<string, TokenArtifact> = ${JSON.stringify(artifact, null, 2)};\n`;
writeFileSync(OUT, ts);
console.log(`Wrote ${OUT} with solc ${solc.version()}`);
